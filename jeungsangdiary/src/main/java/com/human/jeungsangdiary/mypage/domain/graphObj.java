package com.human.jeungsangdiary.mypage.domain;

public class graphObj {

	private String dttm;
	private int num;
	
	
	
	public graphObj(String dttm, int num) {
		super();
		this.dttm = dttm;
		this.num = num;
	}
	
	public String getDttm() {
		return dttm;
	}
	public void setDttm(String dttm) {
		this.dttm = dttm;
	}
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	
	

}
